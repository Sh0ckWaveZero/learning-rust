/// การสร้างฟังก์ชันใน Rust จะใช้คำว่า `fn` ตามด้วยชื่อฟังก์ชัน และวงเล็บปีกกา `{}`
/// ในการระบุขอบเขตของฟังก์ชัน โดยสามารถใช้ `pub` หน้า `fn`
/// เพื่อระบุว่าฟังก์ชันนี้สามารถเรียกใช้จากไฟล์อื่นได้
///
/// กฎการตั้งชื่อฟังก์ชันใน Rust คือ `snake_case`
/// คือใช้ตัวอักษรเล็กทั้งหมดและใช้ `_` ในการแบ่งคำ
///
pub fn hello(item: &str) -> String {
    "Hello, Crabby!".to_string() + item // สามารถใช้ return หรือไม่ใช้ก็ได้;
}
